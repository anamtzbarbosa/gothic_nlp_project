SYSTEM_PROMPT = (
    "You are evaluating text generated by small LSTM language models (~10M parameters) "
    "trained on 3.5 million tokens of 19th-century Gothic fiction. "
    "Expect archaic vocabulary, imperfect grammar, and occasional wandering — that is normal. "
    "For each criterion, pick which continuation (A or B) is better, or call it a tie. "
    "Judge each criterion independently. Be willing to call a tie if both are similarly good or poor."
)

PAIRWISE_TEMPLATE = """\
Two small Gothic language models were given the same prompt. \
For each criterion below, pick which continuation is better: A, B, or tie.

PROMPT:
{prompt}

CONTINUATION A:
{text_a}

CONTINUATION B:
{text_b}

Judge each criterion independently:

1. COHERENCE — does the text hold together logically and follow from the prompt?
2. GOTHIC_ATMOSPHERE — darkness, mystery, dread, supernatural foreboding
3. NARRATIVE_QUALITY — any sense of scene, character, or story development
4. FLUENCY — readability, grammar, and natural word flow

For each criterion choose A, B, or tie.
"""


class CriterionVerdict(BaseModel):
    winner: Literal["A", "B", "tie"]


class PerCriterionResult(BaseModel):
    coherence:         CriterionVerdict
    gothic_atmosphere: CriterionVerdict
    narrative_quality: CriterionVerdict
    fluency:           CriterionVerdict


def judge_once(client, prompt: str, text_a: str, text_b: str, retries: int = 4) -> Optional[PerCriterionResult]:
    content = PAIRWISE_TEMPLATE.format(
        prompt=prompt.strip(),
        text_a=text_a.strip(),
        text_b=text_b.strip(),
    )
    for attempt in range(retries):
        try:
            response = client.models.generate_content(
                model=GEMINI_MODEL,
                contents=content,
                config=types.GenerateContentConfig(
                    system_instruction=SYSTEM_PROMPT,
                    response_mime_type="application/json",
                    response_schema=PerCriterionResult,
                ),
            )
            return PerCriterionResult.model_validate_json(response.text)
        except Exception as e:
            wait = 2 ** attempt + 15
            print(f"  Gemini error (attempt {attempt+1}): {e}")
            time.sleep(wait)
    return None


def pairwise_score(client, prompt: str, lstm_text: str, attn_text: str, n_calls: int = 3):
    """
    Run n_calls pairwise judgments with alternating order.
    Returns per-criterion tallies: {criterion: (lstm_wins, attn_wins, ties)}
    """
    tallies = {c: [0, 0, 0] for c in CRITERIA}  # [lstm_wins, attn_wins, ties]

    for i in range(n_calls):
        if i % 2 == 0:
            a_text, b_text = lstm_text, attn_text
            a_model, b_model = "lstm", "attn"
        else:
            a_text, b_text = attn_text, lstm_text
            a_model, b_model = "attn", "lstm"

        result = judge_once(client, prompt, a_text, b_text)
        if result is None:
            continue

        for c in CRITERIA:
            verdict = getattr(result, c).winner
            if verdict == "tie":
                tallies[c][2] += 1
            elif verdict == "A":
                if a_model == "lstm":
                    tallies[c][0] += 1
                else:
                    tallies[c][1] += 1
            else:  # B
                if b_model == "lstm":
                    tallies[c][0] += 1
                else:
                    tallies[c][1] += 1

        time.sleep(0.5)

    return {c: tuple(tallies[c]) for c in CRITERIA}


def generate_lstm(model, tokenizer, prompt_ids, seq_length, temperature, top_p, device):
    generated_ids = generate_continuation(
        model=model, prompt_ids=prompt_ids, seq_length=seq_length,
        max_new_tokens=seq_length - len(prompt_ids) if seq_length > len(prompt_ids) else seq_length,
        temperature=temperature, top_p=top_p, device=device,
    )
    return tokenizer.decode(generated_ids[len(prompt_ids):])


def generate_attn(model, tokenizer, prompt_text, seq_length, temperature, top_p, device):
    max_new = seq_length - len(tokenizer.encode(prompt_text))
    if max_new <= 0:
        max_new = seq_length
    full_text = attn_generate(
        model=model, tokenizer=tokenizer, prompt=prompt_text,
        seq_length=seq_length, max_new_tokens=max_new,
        temperature=temperature, top_p=top_p, device=device,
    )
    return full_text[len(prompt_text):]


def win_rate(lw, aw):
    total = lw + aw
    if total == 0:
        return 0.0, 0.0
    return lw / total * 100, aw / total * 100


