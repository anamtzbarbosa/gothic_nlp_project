"""
Per-criterion pairwise G-Eval: LSTM-3 vs Attn-LSTM-3 using Gemini as judge.

For each prompt:
  1. Generate a continuation from each model
  2. Show both to Gemini — ask which wins on each criterion separately
  3. Alternate A/B order across 3 calls to cancel position bias
  4. Report win rate per criterion and overall
"""

